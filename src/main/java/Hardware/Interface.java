    private void finalizar_compraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizar_compraActionPerformed
        int verifica_estoque = 0;
        if(carrinho.getSaldoCliente() >= carrinho.SomaTotal()){//verifica se o comprador possui saldo suficiente para a compra
           
            for (int i = 0; i < carrinho.getProdutos().size(); i++) {//procura os produtos do carrinho no estoque
            
                Produtos p = new Produtos(carrinho.getProdutos().get(i));
                
                Connection con = Conexao_BD_hardware.getConnection();//conectar com o banco de dados
                PreparedStatement pstm = null;
                ResultSet rs = null;

                try {
                    pstm =  con.prepareStatement("select * from "+ p.getGenero() +" WHERE id = "+p.getId()+";");
                    rs = pstm.executeQuery();
                     while(rs.next()){
                         verifica_estoque = rs.getInt("quantidade");
                         
                         if(verifica_estoque >= p.getQuantidade()){//se tiver estoque remove  a quantidade desejada 
                             
                             Connection connect = Conexao_BD_hardware.getConnection();//conecta ao banco de dados
                                PreparedStatement atmt = null;
                                String diretorio = "UPDATE "+ p.getGenero() +" SET quantidade = ?  WHERE id = ?";
                                try{
                                atmt = connect.prepareStatement(diretorio);

                                atmt.setInt(1,verifica_estoque - p.getQuantidade());
                                atmt.setInt(2, p.getId());

                                atmt.executeUpdate();

                                }catch(SQLException ex){

                                    JOptionPane.showMessageDialog(null, "Erro ao remover produto do estoque");
                                }finally{
                                   try {
                                       Conexao_BD_hardware.closeConnection(connect, atmt);
                                   } catch (SQLException ex) {
                                       Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
                                   }
                                }
                             
                         }else{
                             JOptionPane.showMessageDialog(null, "Infelizmente "+p.getGenero()+" "+p.getModelo()+" está fora de estoque","ERRO",JOptionPane.ERROR_MESSAGE);
                             JOptionPane.showMessageDialog(null, "Realize a compra novamente");
                             carrinho.getProdutos().remove(i);
                             return;
                             
                         }
                     }

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Produto não encontrado");

                }finally{
                    try {
                        Conexao_BD_hardware.closeConnection(con, pstm);
                    } catch (SQLException ex) {
                        Logger.getLogger(ProcessadorBD.class.getName()).log(Level.SEVERE, null, ex);

                    }
                }
                         
            }
            String produtos = "";
            //adicionar pedido no bd
            for (int i = 0; i < carrinho.getProdutos().size(); i++) {
                    produtos = produtos + carrinho.getProdutos().get(i).getGenero()+" "+carrinho.getProdutos().get(i).getMarca() +" "+carrinho.getProdutos().get(i).getModelo()+ 
                            "       Quantidade: "+carrinho.getProdutos().get(i).getQuantidade()+" \n";
                    
            } 
            
            
            //coloca o registro do pedido no banco de dados
            PedidosBD pedidos_bd = new PedidosBD();
            pedidos_bd.create(carrinho, produtos);

          //______________________________________________________________________  
                    
                    carrinho.setSaldoCliente(carrinho.getSaldoCliente() - carrinho.SomaTotal());//remove valor das compras
                    
            Connection conectar = Conexao_BD_hardware.getConnection();//conecta ao banco de dados
            PreparedStatement atmt = null;
            String diretorio = "UPDATE cliente SET saldo = ?  WHERE id = ?";//atualiza saldo
            try{
            atmt = conectar.prepareStatement(diretorio);

            atmt.setDouble(1,carrinho.getSaldoCliente());
            atmt.setInt(2, carrinho.getId());
            
            atmt.executeUpdate();

            }catch(SQLException ex){

                JOptionPane.showMessageDialog(null, "Erro ao remover produto do estoque");
            }finally{
               try {
                   Conexao_BD_hardware.closeConnection(conectar, atmt);
               } catch (SQLException ex) {
                   Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
               }
            }
                    JOptionPane.showMessageDialog(null, "Compra realizada com sucesso! \n Saldo disponível: "+String.format("%.2f", carrinho.getSaldoCliente()));
                    aba_carrinho.setVisible(false);
                    aba_produtos.setVisible(true);
                    tabela_produto_cliente.setModel(atualiza_lista_produtos());
              
            //limpar carrinho        
            for (int i = 0; i < carrinho.getProdutos().size(); i++) {
                carrinho.getProdutos().remove(i);
            }
            DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Gênero","ID","Marca","Modelo","Preço","Quantidade"},0);
                for (Produtos p:carrinho.getProdutos()) {

                    Object linha[] = new Object[]{p.getGenero(),p.getId(),p.getMarca(),p.getModelo(),p.getPreco(),
                    p.getQuantidade()};
                    modelo.addRow(linha);

                }
                produtos_carrinho.setModel(modelo);
                valortotal_text.setText("R$ "+String.format("%.2f", carrinho.SomaTotal()));
         
        }else{
            JOptionPane.showMessageDialog(null, "Saldo insuficiente \n Saldo disponível: "+String.format("%.2f", carrinho.getSaldoCliente()));
        }
      
    }//GEN-LAST:event_finalizar_compraActionPerformed

