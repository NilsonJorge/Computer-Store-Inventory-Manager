    private void botao_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_loginActionPerformed
        
        ClienteBD aux_cliente = new ClienteBD();
        for (Cliente p:aux_cliente.listar()) {//verifica se é um cliente
            if((usuario_text.getText().equals(p.getLogin()))&&(new String(senha_text.getPassword()).equals(p.getSenha()))){
            aviso_login.setText("Correto");
            aba_user.setVisible(true);
            login_painel.setVisible(false);
            //thread propaganda
            prop1 = new Threads("Faça suas Compras de Natal !!!",anúncio_campo,4000);
            prop2 = new Threads("Veja as promoções !!!",anúncio_campo,7500);
            prop1.start();
            prop2.start();
            
            add_fundo_text.setText("0");
            carrinho = new Carrinho(p.getNome(),p.getEndereco(),p.getSaldo());//inicia carrinho com os dados do cliente
            carrinho.setId(p.getId());
            dados_text_inicio.setText("Nome: "+carrinho.getCliente()+"\nEndereço: "+carrinho.getEndereço()+"\n Saldo: "+String.format("%.2f", carrinho.getSaldoCliente()));
            
            break;
            }
        }
        AdministradorBD aux_admin = new AdministradorBD();
            for (Administrador a:aux_admin.listar()) {
                if((usuario_text.getText().equals(a.getLogin()))&&(new String(senha_text.getPassword()).equals(a.getSenha()))){
                aviso_login.setText("Correto");
                aba_admin.setVisible(true);
                login_painel.setVisible(false);
                break;
            }
        }
            aviso_login.setText("Usuário ou senha incorreto");
            usuario_text.setText("");
            senha_text.setText("");
        
    }//GEN-LAST:event_botao_loginActionPerformed

