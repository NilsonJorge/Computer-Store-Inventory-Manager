    private void remove_promo_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remove_promo_btActionPerformed
       int index = tabela_promocao_ativa.getSelectedRow();
       int aux_id = (Integer) tabela_promocao_ativa.getValueAt(index, 1);
       String aux_genero  = (String)tabela_promocao_ativa.getValueAt(index, 0);
       int estoque_total = 0;
       double preco_antigo = 0;
       int estoque_promo = 0;
       
       Connection con = Conexao_BD_hardware.getConnection();//conectar com o banco de dados
        PreparedStatement pstm = null;
        ResultSet rs = null;
        
        try {
            pstm =  con.prepareStatement("select * from "+ aux_genero +" WHERE id = "+aux_id+";");
            rs = pstm.executeQuery();
             while(rs.next()){
            estoque_total = rs.getInt("aux_quantidade");
            preco_antigo = rs.getDouble("aux_preco");
            estoque_promo = rs.getInt("quantidade");
             }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao ler dados");
            
        }finally{
            try {
                Conexao_BD_hardware.closeConnection(con, pstm);
            } catch (SQLException ex) {
                Logger.getLogger(ProcessadorBD.class.getName()).log(Level.SEVERE, null, ex);
                
            }
        }
        estoque_total = estoque_total + estoque_promo;
        //atualizar produto
        Connection connect = Conexao_BD_hardware.getConnection();//conecta ao banco de dados
        PreparedStatement atmt = null;
        String diretorio = "UPDATE "+ aux_genero +" SET preco = ?,quantidade = ?,aux_preco = ?, aux_quantidade = ? WHERE id = ?";
        try{
        atmt = connect.prepareStatement(diretorio);
      
        atmt.setDouble(1, preco_antigo);
        atmt.setInt(2,estoque_total);
        atmt.setDouble(3, 0);
        atmt.setInt(4, 0);
        atmt.setInt(5, aux_id);
        
        
        atmt.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Promoção finalizada com sucesso");
        }catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null, "Erro ao finalizar promoção");
        }finally{
           try {
               Conexao_BD_hardware.closeConnection(connect, atmt);
           } catch (SQLException ex) {
               Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
           }
        }
       
        atualiza_tabela_sem_desconto();
        atualiza_tabela_promocao_ativa();
        
        remove_promo_bt.setEnabled(false);
       
    }//GEN-LAST:event_remove_promo_btActionPerformed

