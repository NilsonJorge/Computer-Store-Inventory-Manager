    public void atualiza_estoque_preco(int id, double preco, int quantidade, String genero) throws SQLException{
        
        Connection con = Conexao_BD_hardware.getConnection();//conecta ao banco de dados
        PreparedStatement atmt = null;
        String diretorio = "UPDATE "+ genero +" SET preco = ?,quantidade = ? WHERE id = ?";
        try{
        atmt = con.prepareStatement(diretorio);
      
        atmt.setDouble(1, preco);
        atmt.setInt(2, quantidade);
        atmt.setInt(3, id);
        
        
        atmt.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Atualizado com sucesso");
        }catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null, "Erro ao atualizar");
        }finally{
            Conexao_BD_hardware.closeConnection(con, atmt);
        }
    }
    
