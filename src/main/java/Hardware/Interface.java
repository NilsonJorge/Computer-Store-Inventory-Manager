    private void add_fundo_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_fundo_btActionPerformed
        double aux_fundo = Double.parseDouble(add_fundo_text.getText());
        double fundos = aux_fundo + carrinho.getSaldoCliente();
        
        Connection connect = Conexao_BD_hardware.getConnection();//conecta ao banco de dados
            PreparedStatement atmt = null;
            String diretorio = "UPDATE cliente SET saldo = ?  WHERE id = ?";//atualiza saldo
            try{
            atmt = connect.prepareStatement(diretorio);

            atmt.setDouble(1,fundos);
            atmt.setInt(2, carrinho.getId());
            carrinho.setSaldoCliente(fundos);
            atmt.executeUpdate();

            }catch(SQLException ex){

                JOptionPane.showMessageDialog(null, "Erro ao remover produto do estoque");
            }finally{
               try {
                   Conexao_BD_hardware.closeConnection(connect, atmt);
               } catch (SQLException ex) {
                   Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
               }
            }
        add_fundo_text.setText("0");
        
        dados_text_inicio.setText("Nome: "+carrinho.getCliente()+"\nEndereço: "+carrinho.getEndereço()+"\n Saldo: "+String.format("%.2f", carrinho.getSaldoCliente()));
        
    }//GEN-LAST:event_add_fundo_btActionPerformed

